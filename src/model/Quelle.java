package model;

/**
 * Eine Quelle ist eine Entität, von der aus eine Regel ausgeführt werden kann.
 * Es kann sich dabei um einen Würfel, einen Helden oder um einen Schatz handeln.<br>
 * @author Pascal
 *
 */
public interface Quelle {

	/**
	 * casted diese Quelle als Würfel und gibt diesen zurück
	 */
	public abstract Wuerfel alsWuerfel();

	/**
	 * casted diese Quelle als Helden und gibt diesen zurück
	 */
	public abstract Held alsHeld();

	/**
	 * casted diese Quelle als Schatz und gibt diesen zurück
	 */
	public abstract Schatz alsSchatz();

}
